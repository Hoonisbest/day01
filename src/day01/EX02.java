package day01;

public class EX02 {
	public static void main(String[] args) {
		/*
		\n : 엔터와 비슷하다
		\t : 탭크기 만큼 오른쪽으로 이동
			 비어있는 칸을 만들고 채운뒤 남은칸만큼 띄워줌
		\" : "는 일반 문자로 사용할때
		\\ : \을 표현할때 사용 경로 지정시 사용
		 */
		//"" :쌍 따옴표로 묶인 값은 문자열로 인식한다
		System.out.println("H\ne\nl\nl\no Java");
		System.out.println("1\t1234567\t12345678\t12");
		System.out.println("\"test\" 123 \\456");
	}
}
